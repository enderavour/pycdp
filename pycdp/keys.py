class HotKey(Enum):
    Default = 0
    Alt = 1
    Ctrl = 2
    Command = 4
    Shift = 8

    @property
    def key_code(self):
        return {
            HotKey.Ctrl: 17,
            HotKey.Alt: 18,
            HotKey.Shift: 16,
            HotKey.Command: 91
        }.get(self, 0)